#include "fdf.h"

void	file_orders(char *line, int *i, int *x, int *y)
{
	while (line[*i])
	{
		if (line[*i] != '0' || line[*i] != ' ')
			break;
		*x += 50;
		(*i)++;
	}
	*y += 50;
}